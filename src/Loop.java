
public class Loop {

	public static void main(String[] args) {
		
		// For Loop
		for(int i=1;i<5;i++){  
	        System.out.println(i);

	    }
		// While Loop
		 int i=1;  
		 while(i<=5){  
		     System.out.println(i);  
		    i++;  
	     }
		 
		 //Do While Loop
		 int j = 10;
	       do{
		       System.out.println(j); //10
		       j = j + 1; //11
	       }while(j<11);
}
}	
